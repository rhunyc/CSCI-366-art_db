Purpose:<br>
I'm an artist in my freetime and sometimes I will attend nerdy conventions and sell prints of my art work. As the number of prints I've created has grown drastically, I've found it
 terribly difficult to keep track of what prints I have and how many of each size. So this is my solution to that problem.<br>
